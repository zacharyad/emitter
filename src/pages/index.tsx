import { type NextPage } from "next";
import Head from "next/head";
import { useUser, SignInButton, SignOutButton } from "@clerk/nextjs";
import { api } from "~/utils/api";

const Home: NextPage = () => {
  const { data } = api.posts.getAll.useQuery();
  const user = useUser();

  console.log("Hey, ", data);

  return (
    <>
      <Head>
        <title>Create T3 App</title>
        <meta name="description" content="Generated by create-t3-app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <main className="flex min-h-screen flex-col items-center justify-center bg-gradient-to-b from-[#2e026d] to-[#15162c]">
        <h1 className="text-white">Emitter App!</h1>
        {!!user.isSignedIn && (
          <p className="text-white">Welcome {user.user.username}</p>
        )}
        {!user.isSignedIn && (
          <>
            <SignInButton mode="modal">
              <button className="btn border-zinc-300 text-white">
                Sign In
              </button>
            </SignInButton>
          </>
        )}
        {!!user.isSignedIn && (
          <SignOutButton>
            <button className="btn border-zinc-300 text-white">Sign Out</button>
          </SignOutButton>
        )}

        {data?.map(({ content, id }) => {
          return (
            <>
              <p className="text-white" key={id}>
                {content}
              </p>
            </>
          );
        })}
      </main>
    </>
  );
};

export default Home;
